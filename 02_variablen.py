# Variablen sind in Python eine dynamisch Typisiert.
# Das heißt der Inhalt und der Typ kann sich während der Laufzeit des Programms jederzeit ändern.


x = "Hallo Welt!"
print(x)
print(type(x))

# Integer
x = 40
x += 2
print(x)
print(type(x))

# Boolean
x = True
x = x and False
print(x)
print(type(x))

# Float
x = 23.0
x += .2
print(x)
print(type(x))

x = "Hallo"
x += " Welt!"
print(x)
print(type(x))

